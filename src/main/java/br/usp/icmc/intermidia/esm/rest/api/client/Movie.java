package br.usp.icmc.intermidia.esm.rest.api.client;

public class Movie {

	private String name;

	private int year;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

}
